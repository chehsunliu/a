// 2^32 = (2^10)^3 * 4 ~= 10^9 * 4
fn main() -> std::io::Result<()> {
    let mut buf = String::new();
    std::io::stdin().read_line(&mut buf)?;

    let mut blocks: Vec<i32> = vec![];
    for (i, c) in buf.trim().chars().enumerate() {
        let d = c.to_digit(10).unwrap() as i32;
        let v = if i % 2 == 0 { i as i32 / 2 } else { -1 };
        for _ in 0..d {
            blocks.push(v);
        }
    }

    let (mut l, mut r) = (0, blocks.len() - 1);
    while l < r {
        if blocks[l] != -1 {
            l += 1;
            continue;
        }

        if blocks[r] == -1 {
            r -= 1;
            continue;
        }

        blocks.swap(l, r);
    }

    let ans = blocks
        .iter()
        .filter(|id| **id >= 0)
        .enumerate()
        .fold(0u64, |acc, (i, id)| acc + i as u64 * *id as u64);
    println!("{:?}", ans);

    Ok(())
}

